# 13. Write a script to get the maximum and minimum value in a dictionary.


sample = {1: 10, 2: 20, 3: 30, 4: 40, 5: 10, 6: 60, 7: 30, 8: 80}

print("13. Write a script to get the maximum and minimum value in a dictionary.")
print("Sample Dictionary : {}".format(sample))
print("\nMaximum value: {}".format(max(sample.values())))
print("Minimum value: {}".format(min(sample.values())))
